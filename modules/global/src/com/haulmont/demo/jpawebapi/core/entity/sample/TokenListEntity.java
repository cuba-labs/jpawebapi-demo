package com.haulmont.demo.jpawebapi.core.entity.sample;

import com.haulmont.chile.core.annotations.MetaClass;
import com.haulmont.chile.core.annotations.MetaProperty;
import com.haulmont.cuba.core.entity.BaseUuidEntity;
import com.haulmont.cuba.security.entity.User;

import java.util.List;

@MetaClass(name = "ref$TokenListEntity")
public class TokenListEntity extends BaseUuidEntity {

    private static final long serialVersionUID = -2666824364572826503L;

    @MetaProperty
    protected String name;

    @MetaProperty
    protected List<User> users;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public List<User> getUsers() {
        return users;
    }

    public void setUsers(List<User> users) {
        this.users = users;
    }
}